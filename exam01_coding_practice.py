def is_eight(x):
    '''

    :param x: an integer
    :return: true or false statement based on if the given argument is 8
    '''
    eight = x == 8
    return eight

def triple_string(x):
    '''

    :param x: string
    :return: string repeated 3 times
    '''
    triple = 3 * str(x)
    return triple
